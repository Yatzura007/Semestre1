using System;

namespace Programa11U3
{
    class Program
    {
        static void Main(string[] args)
        {
            for (int c = 1; c <= 100; c++)
            {
                Console.WriteLine(c);
            }
            for (int i = 100; i >= 1; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
